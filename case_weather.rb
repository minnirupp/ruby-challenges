weather = "windy"

case weather
when 'sunny'
	puts "It's gonna be a nice and sunny day!"
when 'rainy'
	puts "Gonna need that umbrella!"
when 'snowy'
	puts "Wear that winter coat!"
when 'cloudy'
	puts "It's gonna be a little dreary :("
when 'foggy'
	puts "Drive carefully!"
when 'windy'
	puts "Hold on to your hat!"	
end